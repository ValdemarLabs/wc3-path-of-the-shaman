        /// </summary>
        private void ChkPlainTextMode_CheckedChanged(object sender, EventArgs e)
        {
            if (txtTooltipExtended == null) return;
            
            try
            {
                if (chkPlainTextMode.Checked)
                {
                    // Converting TO plain text mode (remove hex codes, show newlines)
                    string wc3Text = txtTooltipExtended.Text;
                    string plainText = ItemPreviewGenerator.RemoveWC3ColorCodes(wc3Text);
                    txtTooltipExtended.Text = plainText;
                }
                else
                {
                    // Converting FROM plain text TO WC3 format
                    // User has been editing plain text, we need to convert back
                    string plainText = txtTooltipExtended.Text;
                    
                    // Convert newlines to |n
                    string wc3Text = plainText.Replace("\n", "|n").Replace("\r", "");
                    
                    // Note: We don't automatically add color codes when converting back
                    // The user can re-generate if they want colors
                    txtTooltipExtended.Text = wc3Text;
                }
            }
            catch (Exception ex)
            {
                MessageBox.Show($"Error converting text format: {ex.Message}", "Error",
                    MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }
        
        private Color GetRarityColor(string rarity)
        {
            if (string.IsNullOrEmpty(rarity))
                return Color.Gray;
                
            return rarity switch
            {
                "Common" => Color.LightGray,
                "Uncommon" => Color.FromArgb(30, 255, 0),
                "Rare" => Color.FromArgb(0, 112, 221),
                "Epic" => Color.FromArgb(163, 53, 238),
                "Legendary" => Color.FromArgb(255, 128, 0),
                _ => Color.Gray
            };
        }


        private class BaseItemEntry
        {
            public string Code { get; set; }
            public string Name { get; set; }
            public string Type { get; set; }

            public override string ToString()
            {
                if (string.IsNullOrEmpty(Code))
                    return Name;
                return $"[{Code}] {Name} ({Type})";
            }
        }
        
        // Helper class for EQUIP ability dropdown items
        private class EquipAbilityItem
        {
            public string Code { get; set; }
            public string Name { get; set; }
            public string Suffix { get; set; }
            
            public override string ToString()
            {
                if (string.IsNullOrEmpty(Code))
                    return Name; // For "(None)" entry
                return $"{Name} ({Code})";
            }
        }

                    var cmbQuickType = this.Controls.Find("cmbQuickType", true).FirstOrDefault() as ComboBox;
                    if (cmbQuickType != null)
                    {
                        cmbQuickType.Items.Clear();
                        cmbQuickType.Items.AddRange(cmbType.Items.Cast<object>().ToArray());

        private string GenerateItemCode()
        {
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    
                    // Get the highest existing item code
                    string query = @"SELECT item_code FROM items 
                                    WHERE item_code ~ '^[a-z][0-9][a-z][0-9]$' 
                                    ORDER BY item_code DESC LIMIT 1";
                    
                    using (var cmd = new NpgsqlCommand(query, conn))
                    {
                        var result = cmd.ExecuteScalar();
                        
                        if (result != null)
                        {
                            string lastCode = result.ToString();
                            // Parse format: letter + digit + letter + digit (e.g., "a0a0")
                            return IncrementCode(lastCode);
                        }
                        else
                        {
                            // No existing codes, start from i0a0
                            return "i0a0";
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                MessageBox.Show($"Error generating code: {ex.Message}. Using default: i0a0", "Warning",
                    MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return "i0a0";
            }
        }

        private void IncrementItemCode()
        {
            if (!string.IsNullOrEmpty(txtItemCode.Text))
            {
                string currentCode = txtItemCode.Text;
                string newCode = IncrementCode(currentCode);
                txtItemCode.Text = newCode;
                txtItemCode.ReadOnly = false; // Allow editing the new code
            }
        }

        private string IncrementCode(string code)
        {
            if (string.IsNullOrEmpty(code) || code.Length != 4)
                return "i0a0";

            char[] chars = code.ToCharArray();
            
            // Format: letter digit letter digit (e.g., i0a9)
            // Increment from right to left
            
            // Try to increment the last digit (position 3)
            if (char.IsDigit(chars[3]) && chars[3] < '9')
            {
                chars[3]++;
                return new string(chars);
            }
            else if (char.IsDigit(chars[3]))
            {
                chars[3] = '0';
                
                // Increment letter at position 2
                if (char.IsLetter(chars[2]) && chars[2] < 'z')
                {
                    chars[2]++;
                    return new string(chars);
                }
                else if (char.IsLetter(chars[2]))
                {
                    chars[2] = 'a';
                    
                    // Increment digit at position 1
                    if (char.IsDigit(chars[1]) && chars[1] < '9')
                    {
                        chars[1]++;
                        return new string(chars);
                    }
                    else if (char.IsDigit(chars[1]))
                    {
                        chars[1] = '0';
                        
                        // Increment letter at position 0
                        if (char.IsLetter(chars[0]) && chars[0] < 'z')
                        {
                            chars[0]++;
                            return new string(chars);
                        }
                        else
                        {
                            // Wrap around
                            return "i0a0";
                        }
                    }
                }
            }
            
            return "i0a0";
        }


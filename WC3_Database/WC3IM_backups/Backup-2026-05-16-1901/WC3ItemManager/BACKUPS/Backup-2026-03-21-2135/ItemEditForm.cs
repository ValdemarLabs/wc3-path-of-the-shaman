        private void GenerateAbilitiesAndText(string className, string rarityName, int itemLevel, string powerLevel)
        {
            // Only add abilities and flavor text for Strong/Powerful/Godlike
            if (powerLevel == "Minimal" || powerLevel == "Weak" || powerLevel == "Normal")
                return;
            
            Random rng = new Random();
            
            // Generate abilities based on power level and item type
            List<string> abilities = new List<string>();
            List<string> flavorTexts = new List<string>();
            
            bool isWeapon = className.Contains("Weapon") || className.Contains("1h") || className.Contains("2h") || className.Contains("Stave");
            bool isArmor = className.Contains("Armor") || className.Contains("Head") || className.Contains("Chest");
            
            // Power level determines number of abilities
            int abilityCount = powerLevel switch
            {
                "Strong" => rng.Next(0, 2),     // 0-1 abilities
                "Powerful" => rng.Next(1, 3),   // 1-2 abilities  
                "Godlike" => rng.Next(2, 4),    // 2-3 abilities
                _ => 0
            };
            
            // Weapon abilities
            string[] weaponAbilities = {
                "Cleaving Attack", "Bash", "Critical Strike", "Orb of Fire",
                "Orb of Lightning", "Orb of Poison", "Life Drain", "Mana Break"
            };
            
            // Armor abilities
            string[] armorAbilities = {
                "Devotion Aura", "Endurance Aura", "Thorns", "Hardened Skin",
                "Spell Shield", "Evasion", "Unholy Aura", "Brilliance Aura"
            };
            
            // Universal abilities
            string[] universalAbilities = {
                "Vampiric Aura", "Trueshot Aura", "Command Aura", "Regeneration"
            };
            
            // Select abilities
            for (int i = 0; i < abilityCount; i++)
            {
                if (isWeapon && rng.Next(2) == 0)
                    abilities.Add(weaponAbilities[rng.Next(weaponAbilities.Length)]);
                else if (isArmor && rng.Next(2) == 0)
                    abilities.Add(armorAbilities[rng.Next(armorAbilities.Length)]);
                else
                    abilities.Add(universalAbilities[rng.Next(universalAbilities.Length)]);
            }
            
            // Generate flavor text based on power level
            if (powerLevel == "Powerful" || powerLevel == "Godlike")
            {
                string[] powerfulPrefixes = {
                    "Forged in the depths of", "Blessed by the", "Infused with the essence of",
                    "Wielded by the legendary", "Lost artifact of the", "Sacred relic of"
                };
                
                string[] powerfulSuffixes = {
                    "ancient dragons", "fallen gods", "eternal flames", "void itself",
                    "first guardians", "titan kings", "celestial realm", "shadow dimension"
                };
                
                flavorTexts.Add($"{powerfulPrefixes[rng.Next(powerfulPrefixes.Length)]} {powerfulSuffixes[rng.Next(powerfulSuffixes.Length)]}.");
            }
            
            if (powerLevel == "Godlike")
            {
                string[] godlikePhrases = {
                    "Its power knows no bounds",
                    "Reality bends to its will",
                    "Mortals tremble before its might",
                    "The very fabric of existence trembles",
                    "Legends speak of its terror",
                    "None who face it survive"
                };
                flavorTexts.Add(godlikePhrases[rng.Next(godlikePhrases.Length)] + ".");
            }
            
            // Apply to text boxes if they exist
            if (abilities.Count > 0 && txtAbilities != null)
            {
                string currentAbilities = txtAbilities.Text?.Trim();
                string newAbilities = string.Join(",", abilities.Distinct());
                
                if (string.IsNullOrEmpty(currentAbilities))
                    txtAbilities.Text = newAbilities;
                else
                    txtAbilities.Text = currentAbilities + ", " + newAbilities;
            }
            
            // Add flavor text to unified tooltip/description field
            if (flavorTexts.Count > 0 && txtTooltipExtended != null)
            {
                string flavorText = string.Join(" ", flavorTexts);
                string currentText = txtTooltipExtended.Text?.Trim();
                
                if (string.IsNullOrEmpty(currentText))
                    txtTooltipExtended.Text = flavorText;
                else
                    txtTooltipExtended.Text = currentText + " " + flavorText;
            }
        }


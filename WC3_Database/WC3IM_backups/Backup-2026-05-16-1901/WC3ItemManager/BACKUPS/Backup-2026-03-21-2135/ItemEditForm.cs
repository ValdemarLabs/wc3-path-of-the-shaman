        /// <summary>
        /// Update the debug display showing ability names for each ability code.
        /// Called when txtAbilities changes or toggle checkbox is clicked.
        /// </summary>
        private void UpdateAbilityDebugDisplay()
        {
            System.Diagnostics.Debug.WriteLine("[UpdateAbilityDebugDisplay] Called");
            
            if (rtbAbilityDebug == null || chkShowAbilityNames == null)
            {
                System.Diagnostics.Debug.WriteLine("[UpdateAbilityDebugDisplay] NULL: rtbAbilityDebug or chkShowAbilityNames");
                return;
            }

            // Toggle visibility
            rtbAbilityDebug.Visible = chkShowAbilityNames.Checked;
            System.Diagnostics.Debug.WriteLine($"[UpdateAbilityDebugDisplay] Checkbox checked: {chkShowAbilityNames.Checked}");

            if (!chkShowAbilityNames.Checked)
                return;

            try
            {
                string abilitiesText = txtAbilities?.Text?.Trim() ?? "";
                
                if (string.IsNullOrWhiteSpace(abilitiesText))
                {
                    rtbAbilityDebug.Text = "(No abilities defined)";
                    return;
                }

                // Parse ability codes (comma or space separated)
                // Keep duplicates to show accurate count (e.g., A092,A092 for Movement Speed 100%)
                var allAbilityCodes = abilitiesText
                    .Split(new[] { ',', ' ', ';', '\r', '\n' }, StringSplitOptions.RemoveEmptyEntries)
                    .Select(code => code.Trim())
                    .Where(code => !string.IsNullOrWhiteSpace(code))
                    .ToList();

                if (allAbilityCodes.Count == 0)
                {
                    rtbAbilityDebug.Text = "(No valid ability codes found)";
                    return;
                }

                // Count duplicates
                var abilityCounts = new Dictionary<string, int>();
                foreach (var code in allAbilityCodes)
                {
                    if (!abilityCounts.ContainsKey(code))
                        abilityCounts[code] = 0;
                    abilityCounts[code]++;
                }

                // Get unique codes for database lookup
                var uniqueCodes = abilityCounts.Keys.ToList();

                // Query database for ability names
                var abilityInfo = new List<(string code, int count, string name, string suffix)>();
                
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    
                    foreach (var code in uniqueCodes)
                    {
                        int count = abilityCounts[code];
                        string query = "SELECT ability_name, editor_suffix FROM wc3_abilities WHERE ability_code = @code";
                        using (var cmd = new NpgsqlCommand(query, conn))
                        {
                            cmd.Parameters.AddWithValue("code", code);
                            using (var reader = cmd.ExecuteReader())
                            {
                                if (reader.Read())
                                {
                                    string name = reader["ability_name"]?.ToString() ?? "";
                                    string suffix = reader["editor_suffix"]?.ToString() ?? "";
                                    abilityInfo.Add((code, count, name, suffix));
                                }
                                else
                                {
                                    abilityInfo.Add((code, count, "⚠ NOT FOUND IN DATABASE", ""));
                                }
                            }
                        }
                    }
                }

                // Build formatted display
                rtbAbilityDebug.Clear();
                int totalCount = allAbilityCodes.Count;
                int uniqueCount = abilityInfo.Count;
                rtbAbilityDebug.AppendText($"📋 Debug: {totalCount} abilities ({uniqueCount} unique)\n");
                rtbAbilityDebug.AppendText(new string('─', 100) + "\n");

                foreach (var (code, count, name, suffix) in abilityInfo)
                {
                    // Color code based on status
                    string countStr = count > 1 ? $" x{count}" : "";
                    
                    if (name.Contains("NOT FOUND"))
                    {
                        rtbAbilityDebug.SelectionColor = Color.Red;
                        rtbAbilityDebug.AppendText($"❌ {code,-6}{countStr,-4} → NOT FOUND\n");
                    }
                    else if (string.IsNullOrWhiteSpace(name))
                    {
                        rtbAbilityDebug.SelectionColor = Color.Orange;
                        rtbAbilityDebug.AppendText($"⚠  {code,-6}{countStr,-4} → (No Name)");
                        if (!string.IsNullOrWhiteSpace(suffix))
                            rtbAbilityDebug.AppendText($" {suffix}");
                        rtbAbilityDebug.AppendText("\n");
                    }
                    else
                    {
                        rtbAbilityDebug.SelectionColor = Color.Green;
                        rtbAbilityDebug.AppendText($"✓  {code,-6}");
                        if (count > 1)
                        {
                            rtbAbilityDebug.SelectionColor = Color.Blue;
                            rtbAbilityDebug.AppendText($" x{count,-2}");
                        }
                        else
                        {
                            rtbAbilityDebug.AppendText("    ");
                        }
                        rtbAbilityDebug.SelectionColor = Color.Black;
                        rtbAbilityDebug.AppendText($" → {name,-45}");
                        if (!string.IsNullOrWhiteSpace(suffix))
                        {
                            rtbAbilityDebug.SelectionColor = Color.DarkBlue;
                            rtbAbilityDebug.AppendText($" {suffix}");
                        }
                        rtbAbilityDebug.AppendText("\n");
                    }
                    rtbAbilityDebug.SelectionColor = Color.Black; // Reset
                }
            }
            catch (Exception ex)
            {
                rtbAbilityDebug.Text = $"Error loading ability names: {ex.Message}";
                rtbAbilityDebug.SelectionColor = Color.Red;
            }
        }


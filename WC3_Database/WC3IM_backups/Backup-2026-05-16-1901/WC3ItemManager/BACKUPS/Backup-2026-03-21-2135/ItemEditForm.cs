        /// <summary>
        /// Formats ability descriptions from the DataGridView for display in tooltips.
        /// Returns formatted text like "Passive: Flame Aura - Burns nearby units" or "Use: Blink - Teleports the hero"
        /// </summary>
        private string GetFormattedAbilityDescriptions()
        {
            if (dgvManualAbilities == null || dgvManualAbilities.Rows.Count == 0)
                return "";
            
            var descriptions = new List<string>();
            
            foreach (DataGridViewRow row in dgvManualAbilities.Rows)
            {
                if (row.IsNewRow)
                    continue;
                
                string code = row.Cells["Code"].Value?.ToString()?.Trim() ?? "";
                string type = row.Cells["Type"].Value?.ToString()?.Trim() ?? "";
                string description = row.Cells["Description"].Value?.ToString()?.Trim() ?? "";
                
                // Only include abilities with both type and description filled
                if (!string.IsNullOrWhiteSpace(code) && 
                    !string.IsNullOrWhiteSpace(type) && 
                    !string.IsNullOrWhiteSpace(description))
                {
                    // Format: "Passive: Description" or "Use: Description"
                    string formatted = $"|cffffcc00{type}:|r {description}";
                    descriptions.Add(formatted);
                }
            }
            
            if (descriptions.Count == 0)
                return "";
            
            // Join with line breaks and add header
            return "|n|n|cff00ff00Abilities:|r|n" + string.Join("|n", descriptions);
        }

        /// <summary>
        /// Generates the complete WC3-formatted tooltip text including description, stats, and abilities.
        /// This is what gets saved to both ides (Description) and utub (Ubertip) fields.
        /// Format: [Class, Rarity] + Description text + Stats (with color codes) + Abilities
        /// </summary>
        private string GenerateCompleteWC3Tooltip()
        {
            var tooltipParts = new List<string>();
            
            // Part 0: [Class, Rarity] header
            string className = cmbClass?.SelectedItem?.ToString() ?? "";
            string rarityName = cmbRarity?.SelectedItem?.ToString() ?? "";
            
            if (!string.IsNullOrEmpty(className) || !string.IsNullOrEmpty(rarityName))
            {
                // Get colors from color manager if available, otherwise use defaults
                string classColorHex = colorManager?.GetColorHex("class", className) ?? "#A52A2A";
                string rarityColorHex = colorManager?.GetColorHex("rarity", rarityName) ?? "#90EE90";
                
                string classColor = colorManager?.GetWC3ColorCode(classColorHex) ?? "|cffA52A2A";
                string rarityColor = colorManager?.GetWC3ColorCode(rarityColorHex) ?? "|cff90EE90";
                
                tooltipParts.Add($"[{classColor}{className}|r, {rarityColor}{rarityName}|r]");
            }
            
            // Part 1: Extended tooltip text (description/lore) - strip any existing stats to prevent duplication
            string extendedText = txtTooltipExtended?.Text?.Trim() ?? "";
            if (!string.IsNullOrWhiteSpace(extendedText))
            {
                // Strip any WC3-formatted stats that might have been added by AI generation
                // This prevents stats from appearing multiple times after repeated saves
                extendedText = StripStatsFromDescription(extendedText);
                
                // Also remove any old [Class, Rarity] headers
                extendedText = RemoveOldClassRarityHeaders(extendedText);
                
                if (!string.IsNullOrWhiteSpace(extendedText))
                {
                    tooltipParts.Add("|n" + extendedText);
                }
            }
            
            // Part 2: Stats in WC3 color-coded format
            var stats = statsPicker?.GetStatValues();
            if (stats != null && stats.Count > 0)
            {
                var statLines = new List<string>();
                foreach (var stat in stats.OrderBy(s => s.Stat?.DisplayOrder ?? 999))
                {
                    if (stat.Stat != null)
                    {
                        // Convert hex color to WC3 format (|cffRRGGBB)
                        string colorHex = stat.Stat.ColorHex?.Replace("#", "") ?? "FFFFFF";
                        if (colorHex.Length == 6)
                        {
                            colorHex = "ff" + colorHex; // Add alpha channel
                        }
                        
                        // Format stat with color code: |cffRRGGBB+25 Strength|r
                        string statText = $"|c{colorHex}{stat.GetFormattedText()} {stat.Stat.Name}|r";
                        statLines.Add(statText);
                    }
                }
                
                if (statLines.Count > 0)
                {
                    tooltipParts.Add("|n" + string.Join("|n", statLines));
                }
            }
            
            // Part 3: Ability descriptions
            string abilityDescriptions = GetFormattedAbilityDescriptions();
            if (!string.IsNullOrWhiteSpace(abilityDescriptions))
            {
                tooltipParts.Add(abilityDescriptions);
            }
            
            // Combine all parts with proper spacing
            return string.Join("", tooltipParts);
        }
        
        /// <summary>
        /// Removes old [Class, Rarity] style headers from text
        /// </summary>
        private string RemoveOldClassRarityHeaders(string text)
        {
            if (string.IsNullOrWhiteSpace(text))
                return text;
            
            // Remove patterns like [Bracers, Legendary] or [|cffXXXXXXBracers|r, |cffYYYYYYLegendary|r]
            var lines = text.Split(new[] { "|n", "\n" }, StringSplitOptions.None);
            var filteredLines = new List<string>();
            
            foreach (var line in lines)
            {
                string trimmedLine = line.Trim();
                // Skip lines that look like [Class, Rarity] headers
                bool isHeader = trimmedLine.StartsWith("[") && 
                               trimmedLine.EndsWith("]") &&
                               trimmedLine.Contains(",");
                
                if (!isHeader)
                {
                    filteredLines.Add(line);
                }
            }
            
            return string.Join("|n", filteredLines).Trim();
        }
        
        /// <summary>
        /// Cleans tooltip text for editing by removing stats, headers, and abilities.
        /// Leaves only the user-written description/lore text.
        /// </summary>
        private string CleanTooltipForEditing(string text)
        {
            if (string.IsNullOrWhiteSpace(text))
                return "";
            
            // Step 1: Remove [Class, Rarity] headers
            text = RemoveOldClassRarityHeaders(text);
            
            // Step 2: Remove stat lines
            text = StripStatsFromDescription(text);
            
            // Step 3: Remove ability section (everything from "Abilities:" onward)
            var lines = text.Split(new[] { "|n", "\n" }, StringSplitOptions.None);
            var cleanedLines = new List<string>();
            bool inAbilitySection = false;
            
            foreach (var line in lines)
            {
                string trimmedLine = line.Trim();
                
                // Check if we've hit the abilities section
                if (trimmedLine.Contains("Abilities:") || trimmedLine.Contains("|cff00ff00Abilities:|r"))
                {
                    inAbilitySection = true;
                    continue;
                }
                
                // Skip lines in ability section
                if (inAbilitySection)
                    continue;
                
                cleanedLines.Add(line);
            }
            
            // Join and trim
            string result = string.Join("|n", cleanedLines).Trim();
            
            // Remove excessive line breaks at the end
            while (result.EndsWith("|n|n|n"))
            {
                result = result.Substring(0, result.Length - 3);
            }
            
            return result;
        }
        
        /// <summary>
        /// Strips WC3-formatted stats from description text to prevent duplication in tooltips.
        /// Stats like "|cffRRGGBB+10 Strength|r" are removed.
        /// </summary>
        private string StripStatsFromDescription(string text)
        {
            if (string.IsNullOrWhiteSpace(text))
                return text;
            
            // Remove lines that look like WC3-formatted stats: |cffXXXXXX+/-N StatName|r
            // This is a simple heuristic - matches lines starting with |c and ending with |r
            var lines = text.Split(new[] { "|n", "\n" }, StringSplitOptions.None);
            var filteredLines = new List<string>();
            
            foreach (var line in lines)
            {
                string trimmedLine = line.Trim();
                // Skip lines that look like WC3-formatted stats
                // Format: |cffRRGGBB+25 Strength|r or |cff00FF0050% Magic Damage Taken|r
                bool isStatLine = trimmedLine.StartsWith("|c") && 
                                  trimmedLine.EndsWith("|r") &&
                                  !trimmedLine.Contains(":");  // Not an ability like "Passive: Bash"
                
                if (isStatLine)
                {
                    // Additional check: line should contain a sign (+/-) or start with a number after color code
                    // Extract text between |cffXXXXXX and |r
                    int colorEnd = trimmedLine.IndexOf("|r");
                    if (colorEnd > 10) // At least |cffXXXXXX
                    {
                        string content = trimmedLine.Substring(10, colorEnd - 10).Trim();
                        // Check if content starts with +, -, or a digit (for stats like "50% Damage Taken")
                        if (!string.IsNullOrEmpty(content) && 
                            (content[0] == '+' || content[0] == '-' || char.IsDigit(content[0])))
                        {
                            // This is a stat line, skip it
                            continue;
                        }
                    }
                }
                
                filteredLines.Add(line);
            }
            
            return string.Join("|n", filteredLines).Trim();
        }


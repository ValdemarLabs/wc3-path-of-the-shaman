        private void LoadItem()
        {
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    string query = @"
                        SELECT i.*, r.rarity_name, c.class_name, t.type_name 
                        FROM items i
                        LEFT JOIN item_rarities r ON i.rarity_id = r.id
                        LEFT JOIN item_classes c ON i.class_id = c.id
                        LEFT JOIN item_types t ON i.type_id = t.id
                        WHERE i.id = @id";

                    using (var cmd = new NpgsqlCommand(query, conn))
                    {
                        cmd.Parameters.AddWithValue("id", itemId.Value);
                        using (var reader = cmd.ExecuteReader())
                        {
                            if (reader.Read())
                            {
                                txtItemCode.Text = reader["item_code"]?.ToString() ?? "";
                                txtItemCode.ReadOnly = true; // Don't allow changing item code
                                // Strip WC3 color codes for clean display/editing
                                string rawName = reader["item_name"]?.ToString() ?? "";
                                txtItemName.Text = StripWC3ColorCodes(rawName);
                                
                                string baseId = reader["base_id"]?.ToString() ?? "";
                                if (!string.IsNullOrEmpty(baseId))
                                {
                                    // Try to find and select the base item
                                    for (int i = 0; i < cmbBaseId.Items.Count; i++)
                                    {
                                        var item = cmbBaseId.Items[i] as BaseItemEntry;
                                        if (item != null && item.Code == baseId)
                                        {
                                            cmbBaseId.SelectedIndex = i;
                                            break;
                                        }
                                    }
                                }
                                
                                numLevel.Value = reader["item_level"] != DBNull.Value ? Convert.ToDecimal(reader["item_level"]) : 1;
                                numGoldCost.Value = reader["gold_cost"] != DBNull.Value ? Convert.ToDecimal(reader["gold_cost"]) : 0;
                                numLumberCost.Value = reader["lumber_cost"] != DBNull.Value ? Convert.ToDecimal(reader["lumber_cost"]) : 0;
                                numMaxCharges.Value = reader["max_charges"] != DBNull.Value ? Convert.ToDecimal(reader["max_charges"]) : 0;
                                numMaxStack.Value = reader["max_stack"] != DBNull.Value ? Convert.ToDecimal(reader["max_stack"]) : 0;
                                txtTooltip.Text = reader["tooltip"]?.ToString() ?? "";
                                
                                // Load extended tooltip - clean it up for editing (remove stats/headers)
                                // Stats and headers will be re-added on save via GenerateCompleteWC3Tooltip()
                                string rawTooltip = reader["tooltip_extended"]?.ToString() ?? reader["description"]?.ToString() ?? "";
                                string cleanTooltip = CleanTooltipForEditing(rawTooltip);
                                txtTooltipExtended.Text = cleanTooltip;
                                
                                txtHotkey.Text = reader["hotkey"]?.ToString() ?? "";
                                txtIconPath.Text = reader["icon_path"]?.ToString() ?? "";
                                
                                // Load model path - for existing items, load from DB (even if empty)
                                // Don't use default when loading - only when creating new items
                                txtModelPath.Text = reader["model_path"]?.ToString() ?? "";
                                
                                // Store abilities temporarily - will split them after loading stats
                                string existingAbilities = reader["wc3_abilities"]?.ToString() ?? "";
                                txtAbilities.Text = existingAbilities;
                                
                                // Load attachment abilities
                                string attachmentAbilities = reader["wc3_abilities_attachments"]?.ToString() ?? "";
                                if (txtAbilitiesAttachments != null)
                                    txtAbilitiesAttachments.Text = attachmentAbilities;
                                
                                // Store manual abilities JSON data
                                string manualAbilitiesJson = reader["manual_abilities_data"]?.ToString() ?? "";
                                txtAbilitiesManual.Tag = manualAbilitiesJson; // Store temporarily in Tag
                                
                                // Clear both fields - will populate after loading stats
                                txtAbilitiesStats.Text = "";
                                txtAbilitiesManual.Text = "";
                                
                                chkDroppable.Checked = reader["is_droppable"] != DBNull.Value && Convert.ToBoolean(reader["is_droppable"]);
                                chkSellable.Checked = reader["is_sellable"] != DBNull.Value && Convert.ToBoolean(reader["is_sellable"]);
                                chkPawnable.Checked = reader["is_pawnable"] != DBNull.Value && Convert.ToBoolean(reader["is_pawnable"]);
                                chkActivelyUsed.Checked = reader["actively_used"] != DBNull.Value && Convert.ToBoolean(reader["actively_used"]);
                                chkDroppedOnDeath.Checked = reader["dropped_on_death"] != DBNull.Value && Convert.ToBoolean(reader["dropped_on_death"]);

                                string rarity = reader["rarity_name"]?.ToString();
                                if (!string.IsNullOrEmpty(rarity))
                                    cmbRarity.SelectedItem = rarity;
                                
                                // Update icon preview
                                LoadItemIcon(txtIconPath.Text, rarity);

                                string className = reader["class_name"]?.ToString();
                                if (!string.IsNullOrEmpty(className))
                                    cmbClass.SelectedItem = className;

                                string typeName = reader["type_name"]?.ToString();
                                if (!string.IsNullOrEmpty(typeName))
                                    cmbType.SelectedItem = typeName;

                                string wc3Class = reader["wc3_classification"]?.ToString();
                                if (!string.IsNullOrEmpty(wc3Class))
                                    cmbWC3Classification.SelectedItem = wc3Class;
                            }
                        }
                    }
                }
                
                // Load item stats (suppress auto-generation during load)
                isLoadingItem = true;
                var itemStats = tooltipGenerator.LoadItemStats(itemId.Value);
                statsPicker.SetStatValues(itemStats);
                
                // Now that stats are loaded, auto-generate stat abilities
                AutoGenerateAbilities(showMessages: false);
                
                // Split existing abilities from DB into stat vs manual
                string dbAbilities = txtAbilities.Text?.Trim() ?? "";
                if (!string.IsNullOrWhiteSpace(dbAbilities))
                {
                    var existingList = dbAbilities.Split(new[] { ',', ';' }, StringSplitOptions.RemoveEmptyEntries)
                        .Select(a => a.Trim())
                        .Where(a => !string.IsNullOrWhiteSpace(a))
                        .ToList();
                    
                    // Get auto-generated stat abilities
                    var statAbilities = new HashSet<string>();
                    if (!string.IsNullOrWhiteSpace(txtAbilitiesStats.Text))
                    {
                        var statAbilityList = txtAbilitiesStats.Text.Split(new[] { ',', ';' }, StringSplitOptions.RemoveEmptyEntries)
                            .Select(a => a.Trim())
                            .Where(a => !string.IsNullOrWhiteSpace(a));
                        foreach (var ability in statAbilityList)
                            statAbilities.Add(ability);
                    }
                    
                    // Manual abilities = existing abilities - stat abilities
                    var manualAbilities = existingList.Where(a => !statAbilities.Contains(a)).ToList();
                    txtAbilitiesManual.Text = string.Join(",", manualAbilities);
                    
                    // Populate DataGridView with manual ability details (Code + Type + Description)
                    if (dgvManualAbilities != null)
                    {
                        dgvManualAbilities.Rows.Clear();
                        
                        // Try to load from JSON first (preserves Type and Description)
                        string manualAbilitiesJson = txtAbilitiesManual.Tag as string;
                        if (!string.IsNullOrWhiteSpace(manualAbilitiesJson))
                        {
                            try
                            {
                                var manualAbilitiesData = JsonSerializer.Deserialize<List<ManualAbilityData>>(manualAbilitiesJson);
                                if (manualAbilitiesData != null)
                                {
                                    foreach (var abilityData in manualAbilitiesData)
                                    {
                                        if (!string.IsNullOrWhiteSpace(abilityData.Code))
                                        {
                                            dgvManualAbilities.Rows.Add(
                                                abilityData.Code,
                                                abilityData.Type ?? "",
                                                abilityData.Description ?? ""
                                            );
                                        }
                                    }
                                    System.Diagnostics.Debug.WriteLine($"[LoadItem] Loaded {manualAbilitiesData.Count} manual abilities from JSON");
                                }
                            }
                            catch (Exception ex)
                            {
                                System.Diagnostics.Debug.WriteLine($"[LoadItem] Error deserializing manual abilities: {ex.Message}");
                                // Fall back to code-only loading
                                LoadManualAbilitiesFromCodes(manualAbilities);
                            }
                        }
                        else
                        {
                            // No JSON data, load codes only (legacy behavior)
                            LoadManualAbilitiesFromCodes(manualAbilities);
                        }
                    }
                }
                
                isLoadingItem = false;
                
                // Update item level range validation
                UpdateItemLevelRange();
                
                // Update ability debug display (in case debug checkbox is already checked)
                UpdateAbilityDebugDisplay();
                
                // Update item preview
                UpdateItemPreview();
            }
            catch (Exception ex)
            {
                MessageBox.Show($"Error loading item: {ex.Message}", "Error", MessageBoxButtons.OK, MessageBoxIcon.Error);
            }
        }
        
        private void LoadManualAbilitiesFromCodes(List<string> manualAbilities)
        {
            if (dgvManualAbilities == null) return;
            
            foreach (string code in manualAbilities)
            {
                if (!string.IsNullOrWhiteSpace(code) && code.Length == 4)
                {
                    // Add row with code only, Type and Description empty
                    dgvManualAbilities.Rows.Add(code, "", "");
                }
            }
            System.Diagnostics.Debug.WriteLine($"[LoadManualAbilitiesFromCodes] Loaded {manualAbilities.Count} ability codes (legacy mode)");
        }

        private void BtnSave_Click(object sender, EventArgs e)
        {
            // Basic Validation
            if (string.IsNullOrWhiteSpace(txtItemCode.Text) || txtItemCode.Text.Length != 4)
            {
                MessageBox.Show("Item code must be exactly 4 characters.", "Validation Error", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            if (string.IsNullOrWhiteSpace(txtItemName.Text))
            {
                MessageBox.Show("Item name is required.", "Validation Error", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                return;
            }

            // Smart Assistance Validations (warnings - can proceed)
            var warnings = PerformSmartValidation();
            if (warnings.Count > 0)
            {
                string warningMsg = "Smart Assistance Warnings:\n\n" + string.Join("\n", warnings) + 
                    "\n\nDo you want to save anyway?";
                var result = MessageBox.Show(warningMsg, "Smart Assistance", 
                    MessageBoxButtons.YesNo, MessageBoxIcon.Warning);
                if (result == DialogResult.No)
                    return;
            }

            // Auto-generate basic tooltip if empty
            if (string.IsNullOrWhiteSpace(txtTooltip.Text))
            {
                string itemNameStripped = StripWC3ColorCodes(txtItemName.Text.Trim());
                txtTooltip.Text = $"Buy {itemNameStripped}";
            }

            // Auto-generate abilities from stats before saving
            if (!isLoadingItem)
            {
                AutoGenerateAbilities(showMessages: false);
            }

            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();

                    // Get or create rarity/class/type IDs
                    int rarityId = GetOrCreateRarityId(conn, cmbRarity.SelectedItem?.ToString() ?? "Common");
                    int classId = GetOrCreateClassId(conn, cmbClass.SelectedItem?.ToString() ?? "Head Armor");
                    int typeId = GetOrCreateTypeId(conn, cmbType.SelectedItem?.ToString() ?? "Other");

                    if (itemId.HasValue)
                    {
                        // Update
                        string updateQuery = @"
                            UPDATE items SET
                                item_name = @item_name,
                                base_id = @base_id,
                                rarity_id = @rarity_id,
                                class_id = @class_id,
                                type_id = @type_id,
                                item_level = @item_level,
                                gold_cost = @gold_cost,
                                lumber_cost = @lumber_cost,
                                max_charges = @max_charges,
                                max_stack = @max_stack,
                                tooltip = @tooltip,
                                tooltip_extended = @tooltip_extended,
                                description = @description,
                                hotkey = @hotkey,
                                icon_path = @icon_path,
                                model_path = @model_path,
                                wc3_abilities = @wc3_abilities,
                                wc3_abilities_attachments = @wc3_abilities_attachments,
                                manual_abilities_data = @manual_abilities_data,
                                wc3_classification = @wc3_classification,
                                is_droppable = @is_droppable,
                                is_sellable = @is_sellable,
                                is_pawnable = @is_pawnable,
                                actively_used = @actively_used,
                                dropped_on_death = @dropped_on_death,
                                updated_at = CURRENT_TIMESTAMP
                            WHERE id = @id";

                        using (var cmd = new NpgsqlCommand(updateQuery, conn))
                        {
                            AddParameters(cmd);
                            cmd.Parameters.AddWithValue("id", itemId.Value);
                            cmd.ExecuteNonQuery();
                        }
                    }
                    else
                    {
                        // Insert
                        string insertQuery = @"
                            INSERT INTO items (
                                item_code, item_name, base_id, rarity_id, class_id, type_id,
                                item_level, gold_cost, lumber_cost, max_charges, max_stack,
                                tooltip, tooltip_extended, description, hotkey,
                                icon_path, model_path, wc3_abilities, wc3_abilities_attachments, manual_abilities_data, wc3_classification,
                                is_droppable, is_sellable, is_pawnable, actively_used, dropped_on_death
                            ) VALUES (
                                @item_code, @item_name, @base_id, @rarity_id, @class_id, @type_id,
                                @item_level, @gold_cost, @lumber_cost, @max_charges, @max_stack,
                                @tooltip, @tooltip_extended, @description, @hotkey,
                                @icon_path, @model_path, @wc3_abilities, @wc3_abilities_attachments, @manual_abilities_data, @wc3_classification,
                                @is_droppable, @is_sellable, @is_pawnable, @actively_used, @dropped_on_death
                            ) RETURNING id";

                        using (var cmd = new NpgsqlCommand(insertQuery, conn))
                        {
                            cmd.Parameters.AddWithValue("item_code", txtItemCode.Text.Trim());
                            AddParameters(cmd);
                            var newId = cmd.ExecuteScalar();
                            if (!itemId.HasValue)
                            {
                                itemId = Convert.ToInt32(newId);
                            }
                        }
                    }
                    
                    // Save item stats
                    if (itemId.HasValue)
                    {
                        var stats = statsPicker.GetStatValues();
                        tooltipGenerator.SaveItemStats(itemId.Value, stats);
                    }

                    MessageBox.Show("Item saved successfully!", "Success", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    this.DialogResult = DialogResult.OK; // Close on success
                    
                    // Auto-increment code if checkbox is checked and we just created a new item
                    if (chkAutoIncrementCode.Checked && !itemId.HasValue)
                    {
                        IncrementItemCode();
                    }
                }
            }
            catch (Exception ex)
            {
                var result = MessageBox.Show(
                    $"Error saving item: {ex.Message}\n\nDo you want to try again?",
                    "Error Saving Item",
                    MessageBoxButtons.RetryCancel,
                    MessageBoxIcon.Error);
                
                if (result == DialogResult.Retry)
                {
                    BtnSave_Click(sender, e); // Retry
                }
                // If Cancel, do nothing - stay in edit mode
            }
        }

        private void AddParameters(NpgsqlCommand cmd)
        {
            using (var conn = new NpgsqlConnection(connectionString))
            {
                conn.Open();
                int rarityId = GetOrCreateRarityId(conn, cmbRarity.SelectedItem?.ToString() ?? "Common");
                int classId = GetOrCreateClassId(conn, cmbClass.SelectedItem?.ToString() ?? "Head Armor");
                int typeId = GetOrCreateTypeId(conn, cmbType.SelectedItem?.ToString() ?? "Other");

                // Wrap item name with WC3 color code based on rarity
                string rarityName = cmbRarity.SelectedItem?.ToString() ?? "Common";
                string itemName = WrapItemNameWithRarityColor(txtItemName.Text.Trim(), rarityName);
                cmd.Parameters.AddWithValue("item_name", itemName);
                
                var selectedBaseItem = cmbBaseId.SelectedItem as BaseItemEntry;
                cmd.Parameters.AddWithValue("base_id", selectedBaseItem != null ? (object)selectedBaseItem.Code : DBNull.Value);
                
                cmd.Parameters.AddWithValue("rarity_id", rarityId);
                cmd.Parameters.AddWithValue("class_id", classId);
                cmd.Parameters.AddWithValue("type_id", typeId);
                cmd.Parameters.AddWithValue("item_level", (int)numLevel.Value);
                cmd.Parameters.AddWithValue("gold_cost", (int)numGoldCost.Value);
                cmd.Parameters.AddWithValue("lumber_cost", (int)numLumberCost.Value);
                cmd.Parameters.AddWithValue("max_charges", (int)numMaxCharges.Value);
                cmd.Parameters.AddWithValue("max_stack", (int)numMaxStack.Value);
                cmd.Parameters.AddWithValue("tooltip", txtTooltip.Text);
                
                // Generate complete WC3 tooltip with description + stats + abilities
                string completeTooltip = GenerateCompleteWC3Tooltip();
                
                // Ensure tooltip is never completely empty - at minimum use item name
                if (string.IsNullOrWhiteSpace(completeTooltip))
                {
                    string itemNameStripped = StripWC3ColorCodes(txtItemName.Text.Trim());
                    completeTooltip = itemNameStripped;
                }
                
                // Save to both tooltip_extended (utub) and description (ides) for WC3 export
                cmd.Parameters.AddWithValue("tooltip_extended", completeTooltip);
                cmd.Parameters.AddWithValue("description", completeTooltip);
                
                cmd.Parameters.AddWithValue("hotkey", string.IsNullOrWhiteSpace(txtHotkey.Text) ? DBNull.Value : (object)txtHotkey.Text.Trim());
                cmd.Parameters.AddWithValue("icon_path", txtIconPath.Text);
                cmd.Parameters.AddWithValue("model_path", txtModelPath.Text);
                
                // Ensure combined abilities field is up-to-date before saving
                UpdateCombinedAbilitiesField();
                
                cmd.Parameters.AddWithValue("wc3_abilities", string.IsNullOrWhiteSpace(txtAbilities.Text) ? DBNull.Value : (object)txtAbilities.Text);
                cmd.Parameters.AddWithValue("wc3_abilities_attachments", string.IsNullOrWhiteSpace(txtAbilitiesAttachments?.Text) ? DBNull.Value : (object)txtAbilitiesAttachments.Text);
                
                // Serialize manual abilities (Code + Type + Description) to JSON
                var manualAbilitiesList = new List<ManualAbilityData>();
                if (dgvManualAbilities != null)
                {
                    foreach (DataGridViewRow row in dgvManualAbilities.Rows)
                    {
                        if (row.IsNewRow) continue;
                        
                        string code = row.Cells[0].Value?.ToString()?.Trim() ?? "";
                        string type = row.Cells[1].Value?.ToString()?.Trim() ?? "";
                        string description = row.Cells[2].Value?.ToString()?.Trim() ?? "";
                        
                        if (!string.IsNullOrWhiteSpace(code))
                        {
                            manualAbilitiesList.Add(new ManualAbilityData
                            {
                                Code = code,
                                Type = type,
                                Description = description
                            });
                        }
                    }
                }
                
                string manualAbilitiesJson = manualAbilitiesList.Count > 0 
                    ? JsonSerializer.Serialize(manualAbilitiesList) 
                    : null;
                
                // Add JSONB parameter with explicit type
                if (manualAbilitiesJson != null)
                {
                    var param = new NpgsqlParameter("manual_abilities_data", NpgsqlTypes.NpgsqlDbType.Jsonb)
                    {
                        Value = manualAbilitiesJson
                    };
                    cmd.Parameters.Add(param);
                }
                else
                {
                    cmd.Parameters.AddWithValue("manual_abilities_data", DBNull.Value);
                }
                
                cmd.Parameters.AddWithValue("wc3_classification", cmbWC3Classification.SelectedItem?.ToString() ?? "Permanent");
                cmd.Parameters.AddWithValue("is_droppable", chkDroppable.Checked);
                cmd.Parameters.AddWithValue("is_sellable", chkSellable.Checked);
                cmd.Parameters.AddWithValue("is_pawnable", chkPawnable.Checked);
                cmd.Parameters.AddWithValue("actively_used", chkActivelyUsed.Checked);
                cmd.Parameters.AddWithValue("dropped_on_death", chkDroppedOnDeath.Checked);
            }
        }


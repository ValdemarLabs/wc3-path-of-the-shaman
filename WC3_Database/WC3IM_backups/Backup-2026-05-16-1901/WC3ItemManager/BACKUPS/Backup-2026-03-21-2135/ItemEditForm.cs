        private void LoadDropdownData()
        {
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();

                    // Load Rarities from database
                    cmbRarity.Items.Clear();
                    using (var cmd = new NpgsqlCommand("SELECT rarity_name FROM item_rarities ORDER BY rarity_level", conn))
                    using (var reader = cmd.ExecuteReader())
                    {
                        while (reader.Read())
                            cmbRarity.Items.Add(reader["rarity_name"].ToString());
                    }
                    if (cmbRarity.Items.Count > 0) cmbRarity.SelectedIndex = 0;
                    
                    // Sync Quick Create rarity dropdown
                    var cmbQuickRarity = this.Controls.Find("cmbQuickRarity", true).FirstOrDefault() as ComboBox;
                    if (cmbQuickRarity != null)
                    {
                        cmbQuickRarity.Items.Clear();
                        cmbQuickRarity.Items.AddRange(cmbRarity.Items.Cast<object>().ToArray());
                    }

                    // Load Classes from database
                    cmbClass.Items.Clear();
                    using (var cmd = new NpgsqlCommand("SELECT class_name FROM item_classes ORDER BY class_name", conn))
                    using (var reader = cmd.ExecuteReader())
                    {
                        while (reader.Read())
                            cmbClass.Items.Add(reader["class_name"].ToString());
                    }
                    if (cmbClass.Items.Count > 0) cmbClass.SelectedIndex = 0;
                    
                    // Sync Quick Create class dropdown
                    var cmbQuickClass = this.Controls.Find("cmbQuickClass", true).FirstOrDefault() as ComboBox;
                    if (cmbQuickClass != null)
                    {
                        cmbQuickClass.Items.Clear();
                        cmbQuickClass.Items.AddRange(cmbClass.Items.Cast<object>().ToArray());
                    }

                    // Load Types from database
                    cmbType.Items.Clear();
                    using (var cmd = new NpgsqlCommand("SELECT type_name FROM item_types ORDER BY type_name", conn))
                    using (var reader = cmd.ExecuteReader())
                    {
                        while (reader.Read())
                            cmbType.Items.Add(reader["type_name"].ToString());

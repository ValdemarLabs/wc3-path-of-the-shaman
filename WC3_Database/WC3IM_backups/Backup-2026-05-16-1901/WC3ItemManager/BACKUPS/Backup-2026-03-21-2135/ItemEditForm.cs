        private string StripWC3ColorCodes(string text)
        {
            if (string.IsNullOrEmpty(text)) return text;
            // Remove WC3 color codes like |cXXRRGGBB and |r
            return System.Text.RegularExpressions.Regex.Replace(text, @"\|c[a-fA-F0-9]{8}|\|r", "");
        }

        private string GetWC3ColorCodeForRarity(string rarity)
        {
            return rarity switch
            {
                "Common" => "|c00FFFFFF",      // White
                "Uncommon" => "|c001EFF00",    // Green
                "Rare" => "|c000070DD",        // Blue
                "Epic" => "|c00A335EE",        // Purple
                "Legendary" => "|c00FF8000",   // Orange
                _ => "|c00FFFFFF"               // Default white
            };
        }

        private string WrapItemNameWithRarityColor(string itemName, string rarity)
        {
            if (string.IsNullOrWhiteSpace(itemName)) return itemName;
            
            // Strip any existing color codes first
            string cleanName = StripWC3ColorCodes(itemName);
            
            // Wrap with rarity color
            string colorCode = GetWC3ColorCodeForRarity(rarity);
            return $"{colorCode}{cleanName}|r";
        }

        private void RenderWC3ColorCodes(RichTextBox rtb, string text)
        {
            rtb.Clear();
            
            int i = 0;
            while (i < text.Length)
            {
                // Check for WC3 color code: |cXXRRGGBB or |c00RRGGBB
                if (i < text.Length - 10 && text[i] == '|' && i + 1 < text.Length && text[i + 1] == 'c')
                {
                    // Extract color code
                    string colorCode = text.Substring(i + 2, 8); // XXRRGGBB
                    string rrggbb = colorCode.Substring(2, 6); // RRGGBB
                    
                    try
                    {
                        Color color = ColorTranslator.FromHtml("#" + rrggbb);
                        i += 10; // Skip |cXXRRGGBB
                        
                        // Find the end tag |r or next color code
                        int endPos = text.IndexOf("|r", i);
                        int nextColorPos = text.IndexOf("|c", i);
                        
                        if (endPos == -1) endPos = text.Length;
                        if (nextColorPos != -1 && nextColorPos < endPos) endPos = nextColorPos;
                        
                        string coloredText = text.Substring(i, endPos - i);
                        
                        rtb.SelectionStart = rtb.TextLength;
                        rtb.SelectionLength = 0;
                        rtb.SelectionColor = color;
                        rtb.AppendText(coloredText);
                        
                        i = endPos;
                        if (i < text.Length && text[i] == '|' && i + 1 < text.Length && text[i + 1] == 'r')
                            i += 2; // Skip |r
                        
                        continue;
                    }
                    catch
                    {
                        // Invalid color code, treat as regular text
                    }
                }
                else if (text[i] == '|' && i + 1 < text.Length && text[i + 1] == 'n')
                {
                    // Newline
                    rtb.AppendText("\n");
                    i += 2;
                    continue;
                }
                else if (text[i] == '|' && i + 1 < text.Length && text[i + 1] == 'r')
                {
                    // End color tag, skip
                    i += 2;
                    continue;
                }
                
                // Regular character
                rtb.SelectionStart = rtb.TextLength;
                rtb.SelectionLength = 0;
                rtb.SelectionColor = Color.White;
                rtb.AppendText(text[i].ToString());
                i++;
            }
        }


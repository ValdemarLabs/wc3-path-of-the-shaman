        private void UpdateItemLevelRange()
        {
            // Skip validation if suppressed (e.g., during quick create)
            if (suppressItemLevelValidation)
                return;
                
            try
            {
                string className = cmbClass.SelectedItem?.ToString();
                string rarityName = cmbRarity.SelectedItem?.ToString();

                if (string.IsNullOrEmpty(className) || string.IsNullOrEmpty(rarityName))
                {
                    lblItemLevelRange.Text = "";
                    lblItemLevelRange.ForeColor = Color.Gray;
                    return;
                }

                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    var cmd = new NpgsqlCommand(
                        @"SELECT min_level, max_level 
                          FROM item_level_ranges 
                          WHERE item_class_name = @class AND rarity_name = @rarity", conn);
                    cmd.Parameters.AddWithValue("class", className);
                    cmd.Parameters.AddWithValue("rarity", rarityName);

                    using (var reader = cmd.ExecuteReader())
                    {
                        if (reader.Read())
                        {
                            int minLevel = reader.GetInt32(0);
                            int maxLevel = reader.GetInt32(1);
                            
                            lblItemLevelRange.Text = $"Valid: {minLevel}-{maxLevel}";
                            
                            // Auto-set item level to min value if creating new item or out of range
                            int currentLevel = (int)numLevel.Value;
                            if (!itemId.HasValue || currentLevel < minLevel || currentLevel > maxLevel)
                            {
                                numLevel.Value = minLevel;
                                currentLevel = minLevel;
                            }
                            
                            // Check if current level is in range
                            if (currentLevel < minLevel || currentLevel > maxLevel)
                            {
                                lblItemLevelRange.ForeColor = Color.Red;
                                lblItemLevelRange.Text += " ⚠";
                            }
                            else
                            {
                                lblItemLevelRange.ForeColor = Color.Green;
                            }
                        }
                        else
                        {
                            // No range defined for this class/rarity combination
                            lblItemLevelRange.Text = "(No range defined)";
                            lblItemLevelRange.ForeColor = Color.Gray;
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                // Silently fail - range validation is not critical
                lblItemLevelRange.Text = $"(Error: {ex.Message})";
                lblItemLevelRange.ForeColor = Color.Gray;
            }
        }

        // ===== SMART ASSISTANCE METHODS =====
        
        private List<string> PerformSmartValidation()
        {
            var warnings = new List<string>();

            // 1. Duplicate name detection
            var duplicateName = CheckForDuplicateName();
            if (!string.IsNullOrEmpty(duplicateName))
                warnings.Add($"⚠ Similar item name found: '{duplicateName}'");

            // 2. Missing data alerts
            if (string.IsNullOrWhiteSpace(txtIconPath?.Text))
                warnings.Add("ℹ Missing icon path - item will use default icon");

            if (string.IsNullOrWhiteSpace(txtModelPath?.Text))
                warnings.Add("ℹ Missing model path - item will use default model");

            if (string.IsNullOrWhiteSpace(txtTooltip?.Text))
                warnings.Add("⚠ Missing tooltip - consider auto-generating one");

            if (string.IsNullOrWhiteSpace(txtTooltipExtended?.Text))
                warnings.Add("ℹ Missing extended tooltip/description - item lore is recommended");

            // 3. Balance suggestions
            var balanceWarning = CheckItemBalance();
            if (!string.IsNullOrEmpty(balanceWarning))
                warnings.Add($"💰 {balanceWarning}");

            // 4. Stats validation
            if (statsPicker?.StatCount == 0 && (numLevel.Value > 5))
                warnings.Add("⚠ High-level item with no stats - consider adding stats");

            // 5. Code format check
            var codeWarning = ValidateItemCode();
            if (!string.IsNullOrEmpty(codeWarning))
                warnings.Add($"🔤 {codeWarning}");

            // 6. Basic spell check on name (check for common typos)
            var spellWarning = BasicSpellCheck(txtItemName.Text);
            if (!string.IsNullOrEmpty(spellWarning))
                warnings.Add($"📝 {spellWarning}");

            return warnings;
        }

        private string CheckForDuplicateName()
        {
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    
                    // Check for similar names (case-insensitive, partial match)
                    string query = itemId.HasValue
                        ? "SELECT item_name FROM items WHERE LOWER(item_name) LIKE LOWER(@name) AND id != @id LIMIT 1"
                        : "SELECT item_name FROM items WHERE LOWER(item_name) LIKE LOWER(@name) LIMIT 1";
                    
                    using (var cmd = new NpgsqlCommand(query, conn))
                    {
                        cmd.Parameters.AddWithValue("name", $"%{txtItemName.Text.Trim()}%");
                        if (itemId.HasValue)
                            cmd.Parameters.AddWithValue("id", itemId.Value);
                        
                        var result = cmd.ExecuteScalar();
                        return result?.ToString();
                    }
                }
            }
            catch
            {
                return null;
            }
        }

        private string CheckItemBalance()
        {
            try
            {
                int level = (int)numLevel.Value;
                int cost = (int)numGoldCost.Value;
                
                if (level == 0 || cost == 0)
                    return null;

                // Calculate expected cost based on level (rough formula)
                int expectedMinCost = level * 50;
                int expectedMaxCost = level * 500;

                if (cost < expectedMinCost)
                    return $"Item may be underpriced (Level {level}, Cost {cost}). Expected: {expectedMinCost}-{expectedMaxCost}";
                
                if (cost > expectedMaxCost * 2)
                    return $"Item may be overpriced (Level {level}, Cost {cost}). Expected: {expectedMinCost}-{expectedMaxCost}";

                return null;
            }
            catch
            {
                return null;
            }
        }

        private string ValidateItemCode()
        {
            string code = txtItemCode.Text?.Trim();
            if (string.IsNullOrEmpty(code))
                return null;

            // Check if code follows Blizzard's pattern
            if (!System.Text.RegularExpressions.Regex.IsMatch(code, @"^[a-z][0-9][a-z][0-9]$"))
            {
                return "Item code doesn't follow WC3 pattern (letter-digit-letter-digit, e.g., 'i0a5')";
            }

            // Check if code already exists
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    
                    string query = itemId.HasValue
                        ? "SELECT COUNT(*) FROM items WHERE item_code = @code AND id != @id"
                        : "SELECT COUNT(*) FROM items WHERE item_code = @code";
                    
                    using (var cmd = new NpgsqlCommand(query, conn))
                    {
                        cmd.Parameters.AddWithValue("code", code);
                        if (itemId.HasValue)
                            cmd.Parameters.AddWithValue("id", itemId.Value);
                        
                        int count = Convert.ToInt32(cmd.ExecuteScalar());
                        if (count > 0)
                            return $"Item code '{code}' already exists in database";
                    }
                }
            }
            catch
            {
                // Ignore errors
            }

            return null;
        }

        private string BasicSpellCheck(string text)
        {
            if (string.IsNullOrWhiteSpace(text))
                return null;

            // Check for common typos and issues
            var issues = new List<string>();

            // Multiple spaces
            if (System.Text.RegularExpressions.Regex.IsMatch(text, @"\s{2,}"))
                issues.Add("multiple spaces");

            // Leading/trailing spaces
            if (text != text.Trim())
                issues.Add("leading/trailing spaces");

            // All caps (probably not intended)
            if (text.Length > 5 && text == text.ToUpper())
                issues.Add("all CAPS (may be intentional)");

            // Common typos in item names
            var typos = new Dictionary<string, string>
            {
                { "teh", "the" },
                { "adn", "and" },
                { "fo", "of" },
                { "owrld", "world" },
                { "sowrd", "sword" },
                { "sheild", "shield" },
                { "armro", "armor" }
            };

            foreach (var typo in typos)
            {
                if (text.ToLower().Contains(typo.Key))
                    issues.Add($"possible typo: '{typo.Key}' → '{typo.Value}'");
            }

            return issues.Count > 0 ? "Text issues: " + string.Join(", ", issues) : null;
        }
        
        private void LoadItemIcon(string iconPath, string rarity)
        {
            try
            {
                // Clear previous image
                if (picIconPreview.Image != null)
                {
                    var oldImage = picIconPreview.Image;
                    picIconPreview.Image = null;
                    oldImage.Dispose();
                }
                
                if (string.IsNullOrEmpty(iconPath))
                {
                    picIconPreview.Image = null;
                    picIconPreview.BackColor = GetRarityColor(rarity);
                    return;
                }
                
                // Resolve icon path using configuration
                string fullPath = IconPathConfig.Instance.ResolveIconPath(iconPath);
                
                if (string.IsNullOrEmpty(fullPath))
                {
                    picIconPreview.Image = null;
                    picIconPreview.BackColor = GetRarityColor(rarity);
                    return;
                }
                
                // ALWAYS prefer PNG over BLP if path ends with .blp
                string actualPath = fullPath;
                if (System.IO.Path.GetExtension(fullPath).ToLower() == ".blp")
                {
                    string pngPath = System.IO.Path.ChangeExtension(fullPath, ".png");
                    if (System.IO.File.Exists(pngPath))
                    {
                        // Use PNG version directly (faster and more reliable)
                        actualPath = pngPath;
                    }
                    else if (!System.IO.File.Exists(fullPath))
                    {
                        // Neither BLP nor PNG exists
                        picIconPreview.Image = null;
                        picIconPreview.BackColor = GetRarityColor(rarity);
                        return;
                    }
                }
                
                if (!System.IO.File.Exists(actualPath))
                {
                    picIconPreview.Image = null;
                    picIconPreview.BackColor = GetRarityColor(rarity);
                    return;
                }
                
                string ext = System.IO.Path.GetExtension(actualPath).ToLower();
                
                if (ext == ".png" || ext == ".jpg" || ext == ".jpeg")
                {
                    // Load PNG/JPG directly
                    picIconPreview.Image = Image.FromFile(actualPath);
                    picIconPreview.BackColor = Color.Black;
                }
                else if (ext == ".blp")
                {
                    // BLP file - show info that conversion would be needed
                    picIconPreview.Image = null;
                    picIconPreview.BackColor = Color.FromArgb(60, 60, 90);
                }
                else
                {
                    // Other format not supported
                    picIconPreview.Image = null;
                    picIconPreview.BackColor = Color.FromArgb(60, 60, 90);
                }
            }
            catch (Exception ex)
            {
                // Error loading icon
                picIconPreview.Image = null;
                picIconPreview.BackColor = Color.FromArgb(120, 40, 40);
                Console.WriteLine($"Error loading icon: {ex.Message}");
            }
        }
        

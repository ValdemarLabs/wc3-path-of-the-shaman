        private List<ItemStatValue> GenerateStatsForItem(string className, string rarityName, int itemLevel, string powerLevel)
        {
            var stats = new List<ItemStatValue>();
            var allStats = tooltipGenerator.LoadAllStats();

            // Power level multipliers
            decimal powerMultiplier = powerLevel switch
            {
                "Minimal" => 0.3m,
                "Weak" => 0.6m,
                "Normal" => 1.0m,
                "Strong" => 1.5m,
                "Powerful" => 2.5m,
                "Godlike" => 5.0m,
                _ => 1.0m
            };

            // Bonus stats for higher power levels
            int bonusStatCount = powerLevel switch
            {
                "Minimal" => -1,
                "Weak" => 0,
                "Normal" => 0,
                "Strong" => 1,
                "Powerful" => 2,
                "Godlike" => 4,
                _ => 0
            };

            // Rarity multipliers
            decimal rarityMultiplier = rarityName switch
            {
                "Common" => 1.0m,
                "Uncommon" => 1.5m,
                "Rare" => 2.0m,
                "Epic" => 3.0m,
                "Legendary" => 4.5m,
                _ => 1.0m
            };

            // Item level scaling (much gentler - max 3x at level 500)
            decimal levelMultiplier = 1.0m + (itemLevel / 500.0m);
            
            // Apply power multiplier to all calculations
            decimal finalMultiplier = powerMultiplier * rarityMultiplier * levelMultiplier;

            // Determine item type category
            bool isArmor = className.Contains("Armor") || className.Contains("Head") || 
                          className.Contains("Chest") || className.Contains("Leg") || 
                          className.Contains("Foot") || className.Contains("Hand") ||
                          className.Contains("Shoulders") || className.Contains("Bracers") ||
                          className.Contains("Belt");
            
            bool isWeapon = className.Contains("Weapon") || className.Contains("1h") || 
                           className.Contains("2h") || className.Contains("Stave");
            
            bool isOffhand = className.Contains("Shield") || className.Contains("Off Hand");
            bool isJewelry = className.Contains("Ring") || className.Contains("Amulet") || 
                            className.Contains("Neck") || className.Contains("Trinket");

            // Base stat values with power multiplier (reduced for reasonable values)
            int baseStatValue = (int)(7 * finalMultiplier);
            int baseSecondaryValue = (int)(4 * finalMultiplier);

            // Generate stats based on item type
            Random rng = new Random();
            if (isArmor)
            {
                // Armor: HP + Armor + attribute (reduced HP base from 50 to 30)
                AddStat(stats, allStats, "hp", (int)(30 * finalMultiplier));
                AddStat(stats, allStats, "armor", (int)(3 * finalMultiplier));
                
                // Random primary attribute
                string[] attributes = { "str", "agi", "int" };
                AddStat(stats, allStats, attributes[rng.Next(attributes.Length)], baseStatValue);
                
                // Add bonus stats for higher power levels
                if (bonusStatCount > 0)
                {
                    AddStat(stats, allStats, "hp_regen", baseSecondaryValue / 2);
                    if (bonusStatCount > 1)
                        AddStat(stats, allStats, "dodge", baseSecondaryValue / 2);
                }
            }
            else if (isWeapon)
            {
                // Weapon: Damage + attribute
                AddStat(stats, allStats, "dmg", (int)(10 * finalMultiplier));
                
                // Primary attribute for weapons
                if (className.Contains("Stave") || className.Contains("2h"))
                    AddStat(stats, allStats, "int", baseStatValue);
                else
                    AddStat(stats, allStats, "str", baseStatValue);
                
                // Add attack speed or crit
                if (rng.Next(2) == 0)
                    AddStat(stats, allStats, "aspd", baseSecondaryValue);
                else
                    AddStat(stats, allStats, "crit", baseSecondaryValue);
                
                // Add bonus stats for higher power levels
                if (bonusStatCount > 0)
                {
                    AddStat(stats, allStats, "critdmg", baseSecondaryValue);
                    if (bonusStatCount > 1)
                        AddStat(stats, allStats, "lifesteal", baseSecondaryValue / 3);
                }
            }
            else if (isOffhand)
            {
                // Shield: Armor + HP + block
                AddStat(stats, allStats, "armor", (int)(5 * finalMultiplier));
                AddStat(stats, allStats, "hp", (int)(25 * finalMultiplier));
                AddStat(stats, allStats, "block", baseSecondaryValue);
                
                if (bonusStatCount > 0)
                    AddStat(stats, allStats, "hp_regen", baseSecondaryValue / 2);
            }
            else if (isJewelry)
            {
                // Jewelry: Multiple attributes or special stats
                AddStat(stats, allStats, "str", baseSecondaryValue);
                AddStat(stats, allStats, "agi", baseSecondaryValue);
                AddStat(stats, allStats, "int", baseSecondaryValue);
                
                // Add special stat based on rarity
                if (rarityName != "Common" || bonusStatCount > 0)
                {
                    string[] specialStats = { "crit", "lifesteal", "spell_power" };
                    AddStat(stats, allStats, specialStats[rng.Next(specialStats.Length)], baseSecondaryValue);
                }
                
                if (bonusStatCount > 1)
                {
                    AddStat(stats, allStats, "mp", (int)(20 * finalMultiplier));
                    AddStat(stats, allStats, "mp_regen", baseSecondaryValue / 2);
                }
            }
            else
            {
                // Default: balanced stats
                AddStat(stats, allStats, "str", baseSecondaryValue);
                AddStat(stats, allStats, "agi", baseSecondaryValue);
                AddStat(stats, allStats, "hp", (int)(20 * finalMultiplier));
                
                if (bonusStatCount > 0)
                    AddStat(stats, allStats, "int", baseSecondaryValue);
            }

            // Add bonus stats for higher rarities and power levels
            Random rngBonus = new Random();
            if ((rarityName == "Epic" || rarityName == "Legendary") && powerLevel != "Minimal")
            {
                string[] bonusStats = { "hp_regen", "mp_regen", "ms" };
                AddStat(stats, allStats, bonusStats[rngBonus.Next(bonusStats.Length)], baseSecondaryValue / 2);
            }

            if (rarityName == "Legendary" && powerLevel != "Minimal" && powerLevel != "Weak")
            {
                // Legendary: add resistance
                string[] resistances = { "fire_res", "cold_res", "lightning_res", "poison_res" };
                AddStat(stats, allStats, resistances[rngBonus.Next(resistances.Length)], baseSecondaryValue);
            }
            
            // Godlike power: add ALL resistances and movement speed
            if (powerLevel == "Godlike")
            {
                AddStat(stats, allStats, "fire_res", baseSecondaryValue);
                AddStat(stats, allStats, "cold_res", baseSecondaryValue);
                AddStat(stats, allStats, "lightning_res", baseSecondaryValue);
                AddStat(stats, allStats, "poison_res", baseSecondaryValue);
                AddStat(stats, allStats, "ms", baseSecondaryValue);
            }

            return stats;
        }
        

        private string GenerateRandomItemName(string className, string rarityName, string powerLevel)
        {
            Random rng = new Random();
            
            // Prefixes based on power level
            string[] minimalPrefixes = { "Worn", "Old", "Rusty", "Tattered", "Cracked" };
            string[] weakPrefixes = { "Simple", "Common", "Plain", "Basic", "Rough" };
            string[] normalPrefixes = { "Sturdy", "Fine", "Quality", "Solid", "Reliable" };
            string[] strongPrefixes = { "Superior", "Excellent", "Mighty", "Reinforced", "Enchanted" };
            string[] powerfulPrefixes = { "Epic", "Legendary", "Mythical", "Ancient", "Fabled" };
            string[] godlikePrefixes = { "Divine", "Godlike", "Celestial", "Primordial", "Eternal", "Apocalyptic" };
            
            string[] prefixes = powerLevel switch
            {
                "Minimal" => minimalPrefixes,
                "Weak" => weakPrefixes,
                "Normal" => normalPrefixes,
                "Strong" => strongPrefixes,
                "Powerful" => powerfulPrefixes,
                "Godlike" => godlikePrefixes,
                _ => normalPrefixes
            };
            
            // Suffixes based on rarity
            string[] commonSuffixes = { "", "of the Warrior", "of Protection", "of Strength" };
            string[] uncommonSuffixes = { "of Power", "of the Bear", "of the Eagle", "of Swiftness" };
            string[] rareSuffixes = { "of the Phoenix", "of the Dragon", "of the Titan", "of Legends" };
            string[] epicSuffixes = { "of the Immortals", "of Eternity", "of the Ancients", "of Dominion" };
            string[] legendarySuffixes = { "of the Gods", "of Apocalypse", "of Creation", "of Infinity" };
            
            string[] suffixes = rarityName switch
            {
                "Common" => commonSuffixes,
                "Uncommon" => uncommonSuffixes,
                "Rare" => rareSuffixes,
                "Epic" => epicSuffixes,
                "Legendary" => legendarySuffixes,
                _ => commonSuffixes
            };
            
            // Base names from class
            string baseName = className;
            if (className.Contains("Armor"))
                baseName = className.Replace(" Armor", "");
            if (className.Contains("Weapon"))
                baseName = rng.Next(2) == 0 ? "Blade" : "Weapon";
            
            string prefix = prefixes[rng.Next(prefixes.Length)];
            string suffix = suffixes[rng.Next(suffixes.Length)];
            
            if (string.IsNullOrEmpty(suffix))
                return $"{prefix} {baseName}";
            else
                return $"{prefix} {baseName} {suffix}";
        }
        

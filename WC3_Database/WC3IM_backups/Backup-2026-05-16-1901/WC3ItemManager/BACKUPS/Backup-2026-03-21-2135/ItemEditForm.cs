        /// <summary>
        /// Auto-generate WC3 ability codes based on item stats (button click - with messages).
        /// These abilities provide the mechanical bonuses but are hidden from player view.
        /// </summary>
        /// <summary>
        /// Auto-generate WC3 ability codes based on item stats.
        /// These abilities provide the mechanical bonuses for vanilla inventory compatibility.
        /// </summary>
        /// <param name="showMessages">If true, shows message boxes with results; if false, generates silently</param>
        private void AutoGenerateAbilities(bool showMessages)
        {
            try
            {
                var stats = statsPicker.GetStatValues();
                
                // DEBUG: Log call
                System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Called with {stats?.Count ?? 0} stats, showMessages={showMessages}");
                
                if (stats == null || stats.Count == 0)
                {
                    if (showMessages)
                    {
                        MessageBox.Show("No stats defined on this item. Add stats first, then generate abilities.",
                            "No Stats", MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                    else
                    {
                        // Clear abilities when no stats
                        txtAbilitiesStats.Text = ""; // Clear stat abilities field
                        UpdateCombinedAbilitiesField();
                    }
                    return;
                }

                // Map stat names to ability stat types
                // Note: With vanilla inventory, all stat bonuses are applied via item abilities
                // Dictionary uses OrdinalIgnoreCase, so "Crit" and "crit" are the same - no need for duplicates
                var statTypeMap = new Dictionary<string, string>(StringComparer.OrdinalIgnoreCase)
                {
                    // Critical Strike - matches database "Critical Chance"
                    {"Critical Strike Chance", "Crit"},
                    {"Critical Chance", "Crit"},
                    {"Critical Hit Chance", "Crit"},
                    {"Crit Chance", "Crit"},
                    {"Crit", "Crit"},
                    
                    // Hit Chance - matches database "Hit Chance" and stat 36
                    {"Hit Chance", "Hit"},
                    {"Hit", "Hit"},
                    {"Accuracy", "Hit"},
                    
                    // Block Chance - matches database "Block" and stat 35
                    {"Block Chance", "Block"},
                    {"Block", "Block"},
                    
                    // Dodge Chance - matches database "Dodge Chance" (stat_code: evasion)
                    {"Dodge Chance", "Dodge"},
                    {"Dodge", "Dodge"},
                    
                    // Spell Power % - matches database "Spell Power %" (stat_code: spell_power_pct)
                    {"Spell Power %", "Spell"},
                    {"Spell Resistance", "Spell"},
                    {"Spell Power", "Spell Power Flat"},
                    {"Spell", "Spell"},
                    {"Magic Resistance", "Spell"},
                    
                    // Strength - matches database "Strength" (stat_code: str)
                    {"Strength", "Strength"},
                    {"STR", "Strength"},
                    
                    // Agility - matches database "Agility" (stat_code: agi)
                    {"Agility", "Agility"},
                    {"AGI", "Agility"},
                    
                    // Intelligence - matches database "Intelligence" (stat_code: int)
                    {"Intelligence", "Intelligence"},
                    {"INT", "Intelligence"},
                    
                    // Mana - matches database "Mana" (stat_code: mp)
                    {"Mana", "Mana"},
                    {"Mana Bonus", "Mana"},
                    {"MP", "Mana"},
                    {"Mana Points", "Mana"},
                    
                    // Health/HP - matches database "Health" (stat_code: hp)
                    {"Health", "Health"},
                    {"HP", "HP"},
                    {"Health Bonus", "Health"},
                    {"Hit Points", "Health"},
                    {"Life", "Health"},
                    
                    // HP Regen % - matches database "HP Regen %" (stat_code: hp_regen_pct)
                    {"HP Regen %", "HP Regen %"},
                    
                    // Mana Regen % - matches database "Mana Regen %" (stat_code: mp_regen_pct)
                    {"Mana Regen %", "Mana Regen %"},
                    
                    // Damage - matches database "Damage" (stat_code: dmg)
                    {"Damage", "Damage"},
                    {"Attack Damage", "Damage"},
                    {"DMG", "Damage"},
                    
                    // Melee Damage - matches database "Melee Damage" (stat_code: melee_dmg)
                    {"Melee Damage", "Melee Damage"},
                    
                    // Melee Damage % - matches database "Melee Damage %" (stat_code: melee_dmg_pct)
                    {"Melee Damage %", "Melee Damage %"},
                    
                    // Ranged Damage - matches database "Ranged Damage" (stat_code: ranged_dmg)
                    {"Ranged Damage", "Ranged Damage"},
                    
                    // Ranged Damage % - matches database "Ranged Damage %" (stat_code: ranged_dmg_pct)
                    {"Ranged Damage %", "Ranged Damage %"},
                    
                    // Cleave % - matches database "Cleave %" (stat_code: cleave_pct)
                    {"Cleave %", "Cleave %"},
                    
                    // Cleave Area - matches database "Cleave Area" (stat_code: cleave_area)
                    {"Cleave Area", "Cleave Area"},
                    
                    // Lifesteal - matches database "Lifesteal" (stat_code: lifesteal)
                    {"Lifesteal", "Lifesteal"},
                    
                    // Thorns - matches database "Thorns" (stat_code: thorns_flat)
                    {"Thorns", "Thorns"},
                    
                    // Thorns % - matches database "Thorns %" (stat_code: thorns_pct)
                    {"Thorns %", "Thorns %"},
                    
                    // Armor - matches database "Armor" (stat_code: armor)
                    {"Armor", "Armor"},
                    {"Defense", "Armor"},
                    {"Physical Armor", "Armor"},
                    
                    // Armor % - matches database "Armor %" (stat_code: armor_pct)
                    {"Armor %", "Armor %"},
                    
                    // Magic Damage Taken - matches database "Magic Damage Taken" (stat_code: magic_dmg_taken)
                    {"Magic Damage Taken", "Magic Damage Taken"},
                    
                    // Melee Damage Taken - matches database "Melee Damage Taken" (stat_code: melee_dmg_taken)
                    {"Melee Damage Taken", "Melee Damage Taken"},
                    
                    // Pierce Damage Taken - matches database "Pierce Damage Taken" (stat_code: pierce_dmg_taken)
                    {"Pierce Damage Taken", "Pierce Damage Taken"},
                    
                    // Attack Speed - matches database "Attack Speed" (stat_code: aspd)
                    {"Attack Speed", "Attack Speed"},
                    {"IAS", "Attack Speed"},
                    {"Attack Speed Bonus", "Attack Speed"},
                    {"ASPD", "Attack Speed"},
                    
                    // Movement Speed - matches database "Movement Speed" (stat_code: ms)
                    {"Movement Speed", "Movement Speed"},
                    {"MS", "Movement Speed"},
                    {"Move Speed", "Movement Speed"},
                    {"Speed", "Movement Speed"},
                    
                    // Movement Speed % - matches database "Movement Speed %" (stat_code: ms_pct)
                    {"Movement Speed %", "Movement Speed %"},
                };

                var generatedAbilities = new List<string>();
                var unmappedStats = new List<string>();

                foreach (var statValue in stats)
                {
                    string statName = statValue.Stat?.Name ?? "";
                    int value = (int)statValue.Value;
                    
                    System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Processing stat: {statName} = {value}");

                    if (value == 0)
                        continue; // Skip zero values (negative values are valid for damage reduction stats)

                    // Try to map stat name to ability stat type
                    if (statTypeMap.TryGetValue(statName, out string abilityStatType))
                    {
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Mapped '{statName}' to '{abilityStatType}'");
                        var abilities = StatAbilityMapper.FindAbilityCombination(abilityStatType, value);
                        if (abilities != null && abilities.Count > 0)
                        {
                            System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Found {abilities.Count} abilities: {string.Join(", ", abilities)}");
                            generatedAbilities.AddRange(abilities);
                        }
                        else
                        {
                            System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] No abilities found for {abilityStatType}={value}");
                            
                            // Provide helpful error message with suggestions
                            string errorMsg = $"{statName}: {value}";
                            
                            // Check if the value is too high or if abilities need to be created
                            var availableAbilities = StatAbilityMapper.GetAbilitiesForStat(abilityStatType);
                            if (availableAbilities != null && availableAbilities.Count > 0)
                            {
                                int maxValue = availableAbilities.Max(a => Math.Abs(a.Value));
                                if (Math.Abs(value) > maxValue)
                                {
                                    errorMsg += $" ⚠️ Value too high! Max available: ±{maxValue}. Create higher-value abilities in World Editor.";
                                }
                                else
                                {
                                    errorMsg += $" ⚠️ Cannot combine available abilities to reach exact value. Available values: {string.Join(", ", availableAbilities.Select(a => a.Value).OrderByDescending(Math.Abs).Take(5))}";
                                }
                            }
                            else
                            {
                                errorMsg += " ⚠️ No abilities defined for this stat type in StatAbilityMapper.cs";
                            }
                            
                            unmappedStats.Add(errorMsg);
                        }
                    }
                    else
                    {
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Could not map stat name: {statName}");
                        unmappedStats.Add($"{statName} (unknown stat type)");
                    }
                }

                System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Generated {generatedAbilities.Count} total abilities");
                
                // Update stat abilities field (read-only, auto-generated)
                if (txtAbilitiesStats == null)
                {
                    System.Diagnostics.Debug.WriteLine("[AutoGenerateAbilities] ERROR: txtAbilitiesStats is NULL!");
                    MessageBox.Show("txtAbilitiesStats is NULL!", "ERROR");
                    return;
                }
                
                // Update txtAbilitiesStats field (ONLY ability codes, no error messages)
                if (generatedAbilities.Count > 0)
                {
                    // Show generated ability codes
                    txtAbilitiesStats.Text = string.Join(",", generatedAbilities);
                    
                    // Change background color and show warning if some stats couldn't be mapped
                    if (unmappedStats.Count > 0)
                    {
                        txtAbilitiesStats.BackColor = Color.FromArgb(255, 243, 205); // Light orange warning
                        if (lblAbilityWarning != null)
                        {
                            lblAbilityWarning.Text = $"⚠️ Warning: {unmappedStats.Count} stat(s) couldn't be fully mapped: {string.Join(", ", unmappedStats.Take(2))}";
                            if (unmappedStats.Count > 2)
                                lblAbilityWarning.Text += $" (+{unmappedStats.Count - 2} more)";
                            lblAbilityWarning.ForeColor = Color.DarkOrange;
                            lblAbilityWarning.Visible = true;
                        }
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Generated {generatedAbilities.Count} abilities, but {unmappedStats.Count} stats couldn't be mapped");
                    }
                    else
                    {
                        txtAbilitiesStats.BackColor = Color.FromArgb(240, 255, 240); // Light green success
                        if (lblAbilityWarning != null)
                            lblAbilityWarning.Visible = false;
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Successfully generated {generatedAbilities.Count} abilities");
                    }
                }
                else
                {
                    // No abilities generated
                    txtAbilitiesStats.Text = ""; // Clear field
                    
                    if (unmappedStats.Count > 0)
                    {
                        txtAbilitiesStats.BackColor = Color.FromArgb(255, 205, 210); // Light red error
                        if (lblAbilityWarning != null)
                        {
                            lblAbilityWarning.Text = $"❌ Error: Cannot generate abilities for: {string.Join(", ", unmappedStats.Take(3))}";
                            if (unmappedStats.Count > 3)
                                lblAbilityWarning.Text += $" (+{unmappedStats.Count - 3} more)";
                            lblAbilityWarning.ForeColor = Color.Red;
                            lblAbilityWarning.Visible = true;
                        }
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] ERROR: Cannot generate abilities for {unmappedStats.Count} stats");
                    }
                    else
                    {
                        txtAbilitiesStats.BackColor = Color.FromArgb(240, 240, 240); // Normal gray
                        if (lblAbilityWarning != null)
                            lblAbilityWarning.Visible = false;
                        System.Diagnostics.Debug.WriteLine("[AutoGenerateAbilities] No stats to generate abilities from");
                    }
                }
                
                System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Final txtAbilitiesStats.Text: '{txtAbilitiesStats.Text}'");
                
                // Force update of combined field and debug display
                System.Diagnostics.Debug.WriteLine("[AutoGenerateAbilities] Calling UpdateCombinedAbilitiesField...");
                UpdateCombinedAbilitiesField();
                
                System.Diagnostics.Debug.WriteLine("[AutoGenerateAbilities] Calling UpdateAbilityDebugDisplay...");
                UpdateAbilityDebugDisplay();

                // Display result messages
                if (showMessages)
                {
                    // Manual generation: Show detailed success/failure message
                    if (generatedAbilities.Count > 0)
                    {
                        string message = $"Generated {generatedAbilities.Count} abilities:\n{string.Join(", ", generatedAbilities)}";
                        
                        if (unmappedStats.Count > 0)
                        {
                            message += $"\n\nWarning: Could not map the following stats:\n" + string.Join("\n", unmappedStats);
                            message += "\n\nYou may need to create additional abilities in World Editor\nto support these exact stat values.";
                        }
                        
                        message += "\n\nNote: These abilities are for mechanical bonuses only.\nStats displayed in tooltip come from the stats table, not abilities.";
                        
                        MessageBox.Show(message, "Abilities Generated", 
                            MessageBoxButtons.OK, MessageBoxIcon.Information);
                    }
                    else
                    {
                        MessageBox.Show("No abilities could be generated from the current stats.\n\n" +
                            "Unmapped stats:\n" + string.Join("\n", unmappedStats) +
                            "\n\nYou need to create abilities in World Editor for these stat values.",
                            "No Abilities Generated", MessageBoxButtons.OK, MessageBoxIcon.Warning);
                    }
                }
                else
                {
                    // Silent auto-generation: Don't show MessageBox (already showing warning label)
                    // MessageBox would interrupt LoadItem() flow and prevent manual abilities from being loaded
                    if (unmappedStats.Count > 0)
                    {
                        System.Diagnostics.Debug.WriteLine($"[AutoGenerateAbilities] Silent mode: {unmappedStats.Count} unmappable stats (not showing MessageBox)");
                    }
                }
                
                // Update ability debug display and preview
                UpdateAbilityDebugDisplay();
                UpdateItemPreview();
            }
            catch (Exception ex)
            {
                // Show error in the field so user can see what went wrong
                txtAbilitiesStats.Text = $"ERROR: {ex.Message}";
                txtAbilitiesStats.BackColor = Color.FromArgb(255, 205, 210); // Red
                
                if (lblAbilityWarning != null)
                {
                    lblAbilityWarning.Text = $"❌ Exception: {ex.GetType().Name} - See field above for details";

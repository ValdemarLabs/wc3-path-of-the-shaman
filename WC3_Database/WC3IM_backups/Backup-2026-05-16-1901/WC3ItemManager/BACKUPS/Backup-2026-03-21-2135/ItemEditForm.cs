        private void BtnAutoGenerateTooltip_Click(object sender, EventArgs e)
        {
            try
            {
                string itemName = txtItemName.Text.Trim();
                if (string.IsNullOrWhiteSpace(itemName))
                {
                    MessageBox.Show("Please enter an item name first.", "Missing Information", 
                        MessageBoxButtons.OK, MessageBoxIcon.Warning);
                    return;
                }
                
                string rarityName = cmbRarity.SelectedItem?.ToString() ?? "Common";
                int itemLevel = (int)numLevel.Value;
                var stats = statsPicker.GetStatValues();
                string className = cmbClass.SelectedItem?.ToString() ?? "MISC";
                
                // Generate descriptive text ONLY (no stats - stats shown separately in preview)
                string generatedText = tooltipGenerator.GenerateDescriptiveText(
                    itemName, className, rarityName, itemLevel, stats);
                
                // Append formatted ability descriptions (if any)
                string abilityDescriptions = GetFormattedAbilityDescriptions();
                if (!string.IsNullOrWhiteSpace(abilityDescriptions))
                {
                    generatedText += abilityDescriptions;
                }
                
                string currentText = txtTooltipExtended.Text;
                
                // If current text is empty, apply directly
                if (string.IsNullOrWhiteSpace(currentText))
                {
                    txtTooltipExtended.Text = generatedText;
                    MessageBox.Show("Descriptive text generated successfully!\n\nNote: Stats are displayed automatically from the Stats tab.", "Success", 
                        MessageBoxButtons.OK, MessageBoxIcon.Information);
                    
                    // Update preview to show the new tooltip
                    UpdateItemPreview();
                }
                else
                {
                    // Create regenerate callback that generates new descriptive text
                    Func<string> regenerateCallback = () =>
                    {
                        try
                        {
                            string itemName = txtItemName.Text.Trim();

        private string GenerateNextItemCode(string currentCode)
        {
            // Try to increment last character/number in code
            if (string.IsNullOrEmpty(currentCode) || currentCode.Length < 4)
                return currentCode + "_copy";
            
            // Try to find trailing number and increment
            var match = System.Text.RegularExpressions.Regex.Match(currentCode, @"^(.*?)(\d+)$");
            if (match.Success)
            {
                string prefix = match.Groups[1].Value;
                int number = int.Parse(match.Groups[2].Value);
                return prefix + (number + 1);
            }
            
            // Try incrementing last letter
            char lastChar = currentCode[currentCode.Length - 1];
            if (char.IsLetter(lastChar) && lastChar < 'z' && lastChar < 'Z')
            {
                return currentCode.Substring(0, currentCode.Length - 1) + (char)(lastChar + 1);
            }
            
            return currentCode + "2";
        }
        
        private void EnsureManualAbilitiesColumn()
        {
            try
            {
                using (var conn = new NpgsqlConnection(connectionString))
                {
                    conn.Open();
                    string checkQuery = @"
                        SELECT column_name 
                        FROM information_schema.columns 
                        WHERE table_name = 'items' AND column_name = 'manual_abilities_data'";
                    
                    using (var cmd = new NpgsqlCommand(checkQuery, conn))
                    {
                        var result = cmd.ExecuteScalar();
                        if (result == null)
                        {
                            // Column doesn't exist, create it
                            string alterQuery = "ALTER TABLE items ADD COLUMN manual_abilities_data JSONB";
                            using (var alterCmd = new NpgsqlCommand(alterQuery, conn))
                            {
                                alterCmd.ExecuteNonQuery();
                                System.Diagnostics.Debug.WriteLine("[EnsureManualAbilitiesColumn] Created manual_abilities_data column");
                            }
                        }
                    }
                }
            }
            catch (Exception ex)
            {
                System.Diagnostics.Debug.WriteLine($"[EnsureManualAbilitiesColumn] Error: {ex.Message}");
                // Non-fatal - continue without this feature
            }
        }

